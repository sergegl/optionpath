function templateLeg(
  strategyId: string,
  action: StrategyTemplateLeg["action"],
  type: StrategyTemplateLeg["type"],
  role: string,
  quantity: number,
): StrategyTemplateLeg {
  return {
    id: `leg-template-${strategyId}-${role.toLowerCase().replaceAll(" ", "-")}`,
    strategyId,
    action,
    type,
    role,
    quantity,
  };
}
